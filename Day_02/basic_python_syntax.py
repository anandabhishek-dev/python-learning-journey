# Declaring variables
a = 40
b = 30
c = 60

# Displaying variable values
print("Value of a:", a)
print("Value of b:", b)
print("Value of c:", c)

# Basic arithmetic
print("a + b =", a + b)

# Multiple variable assignment
x, y, z = 2, 4, 7

print("Value of x:", x)
print("x + y =", x + y)

# Reassigning variables
a, b = 2, 3

# Storing the result in another variable
c = a + b

print("New value of c:", c)

